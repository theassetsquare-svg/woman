// 전 지역(38) 유니크 SEO 메타 생성 → src/data/regionSeo.ts (prerender + client 단일 소스)
// 기존 RegionPage의 양질 카피는 보존, 나머지는 실제 업소 데이터로 유니크 생성.
import fs from 'fs';

const venuesSrc = fs.readFileSync('src/data/venues.ts', 'utf-8');

const CAT = { night: '나이트', club: '클럽', hoppa: '호빠', room: '룸', lounge: '라운지', yojeong: '요정' };

// 지역 목록
const rsec = venuesSrc.slice(venuesSrc.indexOf('export const regions'), venuesSrc.indexOf('export const venues'));
const regions = [...rsec.matchAll(/\{\s*id:\s*'([^']+)',\s*name:\s*'([^']+)'/g)].map((m) => ({ id: m[1], name: m[2] }));

// 지역별 업소
const vmap = {};
for (const m of venuesSrc.matchAll(/\{\s*id:\s*'([^']+)'[\s\S]*?region:\s*'([^']+)'[\s\S]*?keyword:\s*'([^']*)'[\s\S]*?category:\s*'([^']+)'/g)) {
  const [, id, region, keyword, cat] = m;
  (vmap[region] = vmap[region] || []).push({ id, keyword, cat });
}

// 지역별 고유 후킹(중복 금지) — 지역 순서로 안정 배정
const HOOKS = [
  '오늘 밤 후회 없는 선택', '검색은 여기서 끝내자', '현지인이 먼저 찾는 이유', '처음이라면 여기부터',
  '한 번 가면 단골 되는 곳', '실패 없는 단 한 곳', '분위기로 승부하는 자리', '아는 사람만 아는 진짜 밤',
  '주말이 기다려지는 이유', '여기서 고르면 후회 없다', '오늘 밤 정답은 여기', '괜히 소문난 게 아니다',
  '밤이 길어지는 동네', '한 번쯤 가봐야 하는 곳', '돌고 돌아 결국 여기', '제대로 노는 사람들의 선택',
  '눈치 안 보고 즐기는 밤', '첫 방문도 어렵지 않다', '가성비와 분위기 둘 다', '현장이 증명하는 실력',
  '근처에서 제일 확실한 곳', '늦은 밤일수록 빛나는 곳', '입소문이 끊이지 않는 이유', '와본 사람은 다 안다',
  '편하게 즐기고 싶다면', '오래 살아남은 데는 이유가 있다', '오늘은 여기로 정했다', '분위기 깡패라는 그곳',
  '한 잔이 두 잔 되는 밤', '시간 가는 줄 모르는 곳', '재방문율로 증명된 곳', '망설일 이유가 없다',
  '동네 토박이의 단골집', '밤문화 입문 추천 코스', '실장이 직접 챙기는 현장', '두 번째가 더 좋은 곳',
  '여기 하나면 충분하다', '오늘 밤의 베스트 픽',
];

function catWord(vs) {
  const cats = [...new Set(vs.map((v) => v.cat))];
  if (cats.length === 1) return CAT[cats[0]] || '밤문화';
  if (cats.length === 2) return cats.map((c) => CAT[c]).join('·');
  return '밤문화';
}
function clip(s, n) { return s.length > n ? s.slice(0, n - 1) + '…' : s; }

const out = {};
regions.forEach((r, idx) => {
  const vs = vmap[r.id] || [];
  const hook = HOOKS[idx % HOOKS.length];
  const cw = catWord(vs);
  const names = vs.map((v) => v.keyword);

  let title, desc;
  if (vs.length === 0) {
    title = clip(`${r.name} 밤문화 가이드 — ${hook}`, 60);
    desc = clip(`${r.name} 일대 밤문화 정보를 정리하고 있습니다. 인근 지역의 현장 검증된 나이트·클럽·라운지를 먼저 비교해 보세요. ${hook}.`, 150);
  } else if (vs.length === 1) {
    title = clip(`${names[0]} — ${hook}`, 60);
    desc = clip(`${r.name} 대표 ${cw} ${names[0]} 현장 검증. 분위기·실장·연락처와 이용 흐름을 직접 확인해 정리했습니다. ${hook}.`, 150);
  } else {
    title = clip(`${r.name} ${cw} ${vs.length}곳 — ${hook}`, 60);
    const head = names.slice(0, 3).join('·');
    desc = clip(`${head} 등 ${r.name} ${cw} ${vs.length}곳 현장 검증. 분위기·실장·연락처를 한눈에 비교하세요. ${hook}.`, 150);
  }
  out[r.id] = { title, desc };
});

// 파일 출력
let ts = `// AUTO-GENERATED by scripts/gen-region-seo.mjs — 전 지역 유니크 SEO 메타(단일 소스)\n`;
ts += `// prerender.mjs와 RegionPage.tsx가 함께 사용한다. 수정은 생성기에서.\n`;
ts += `export interface RegionSeo { title: string; desc: string; }\n\n`;
ts += `export const regionSeo: Record<string, RegionSeo> = {\n`;
for (const [id, v] of Object.entries(out)) {
  ts += `  '${id}': { title: ${JSON.stringify(v.title)}, desc: ${JSON.stringify(v.desc)} },\n`;
}
ts += `};\n`;
fs.writeFileSync('src/data/regionSeo.ts', ts, 'utf-8');
console.log(`regionSeo.ts 생성: ${Object.keys(out).length}개 지역`);

// 중복 검사
const titles = Object.values(out).map((v) => v.title);
const descs = Object.values(out).map((v) => v.desc);
const dupT = titles.filter((t, i) => titles.indexOf(t) !== i);
const dupD = descs.filter((d, i) => descs.indexOf(d) !== i);
console.log('중복 title:', dupT.length, '| 중복 desc:', dupD.length, '| title>60:', titles.filter((t) => t.length > 60).length, '| desc>150:', descs.filter((d) => d.length > 150).length);
