export function getVenueLabel(venue: Venue): string {
  if (venue.keyword) return venue.keyword;
  if (venue.category === 'room') return `${venue.seoArea}룸 ${venue.name}`;
  if (venue.category === 'yojeong') return `${venue.seoArea}요정 ${venue.name}`;
  if (venue.category === 'hoppa') return `${venue.seoArea}호빠 ${venue.name}`;
  if (venue.category === 'club') return `${venue.seoArea}클럽 ${venue.name}`;
  if (venue.category === 'lounge') return `${venue.seoArea}라운지 ${venue.name}`;
  return `${venue.seoArea}나이트 ${venue.name}`;
}

