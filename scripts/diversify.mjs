// Apply venue-specific replacements
for (const [venueId, replacements] of Object.entries(vocabMap)) {
  // Find this venue's content block
  const startPattern = `'${venueId}':`;
  const startIdx = src.indexOf(startPattern);
  if (startIdx === -1) continue;

  // Find the end of this venue block (next venue or end of content object)
  let depth = 0;
  let blockStart = src.indexOf('{', startIdx);
  let blockEnd = blockStart;
  for (let i = blockStart; i < src.length; i++) {
    if (src[i] === '{') depth++;
    if (src[i] === '}') depth--;
    if (depth === 0) { blockEnd = i + 1; break; }
  }

  let block = src.slice(startIdx, blockEnd);

  // Apply replacements (only within this venue block)
  for (const [from, to] of Object.entries(replacements)) {
    // Don't replace inside the venue ID itself or keyword
    block = block.replace(new RegExp(from, 'g'), to);
  }

  src = src.slice(0, startIdx) + block + src.slice(blockEnd);
}

writeFileSync('src/data/venueContent.ts', src);
